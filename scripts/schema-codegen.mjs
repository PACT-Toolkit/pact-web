#!/usr/bin/env node

/**
 * pact.decisions Schema Codegen (json-schema-to-typescript)
 *
 * Generates TypeScript types from the vendored pact.decisions JSON Schema
 * (schema/pact-decisions/pact.decisions.schema.json, mirrored from
 * pact-contracts' decisions/pact.decisions.schema.json via `pnpm api:update`).
 *
 * Also mirrors the vendored benign-label vocabulary (PACT-574) --
 * schema/pact-decisions/benign_labels.json, from pact-contracts'
 * decisions/benign_labels.json -- into a plain TS constant. This one isn't a
 * JSON Schema, so it's a straight read-and-emit rather than a
 * json-schema-to-typescript compile.
 *
 * Usage: node scripts/schema-codegen.mjs
 */

import { execSync } from 'child_process';
import { mkdir, readFile, writeFile } from 'fs/promises';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

import { compile } from 'json-schema-to-typescript';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SCHEMA_DIR = join(ROOT, 'schema/pact-decisions');
const SCHEMA_PATH = join(SCHEMA_DIR, 'pact.decisions.schema.json');
const BENIGN_LABELS_PATH = join(SCHEMA_DIR, 'benign_labels.json');
const OUTPUT_DIR = join(ROOT, 'src/__codegen__/schema/pact-decisions');

const run = (cmd) => execSync(cmd, { stdio: 'inherit', cwd: ROOT });

const bannerFor = (file, source) => `/**
 * Auto-generated from pact-contracts' ${source}
 * (vendored at schema/pact-decisions/${source.split('/').pop()}).
 * Do not edit ${file} manually - run \`pnpm schema:codegen\` to regenerate.
 */
`;

const BARREL_BANNER = `/**
 * Auto-generated barrel for pact-contracts' vendored decisions/ artifacts
 * (schema/pact-decisions/pact.decisions.schema.json and
 * schema/pact-decisions/benign_labels.json).
 * Do not edit this file manually - run \`pnpm schema:codegen\` to regenerate.
 */
`;

async function main() {
  console.log('🚀 pact.decisions Schema Codegen (json-schema-to-typescript)\n');

  const schema = JSON.parse(await readFile(SCHEMA_PATH, 'utf-8'));

  console.log('🔧 Compiling types...');
  // json-schema-to-typescript names the root interface from the schema's own
  // `title` ("pact.decisions") whenever one is present, ignoring the `name`
  // argument below -- it resolves to PactDecisions regardless of what's
  // passed here. The app-facing DecisionPayload alias lives one layer up, in
  // src/app/audit/domain/audit_decision_payload.ts.
  const body = await compile(schema, 'PactDecisions', {
    bannerComment: '',
    additionalProperties: false,
  });

  await mkdir(OUTPUT_DIR, { recursive: true });
  await writeFile(
    join(OUTPUT_DIR, 'decisions.ts'),
    bannerFor('this file', 'decisions/pact.decisions.schema.json') + '\n' + body
  );

  console.log('\n🔧 Emitting benign-label constant...');
  const benignLabels = JSON.parse(await readFile(BENIGN_LABELS_PATH, 'utf-8'));
  const benignLabelsBody = `/**
 * ${benignLabels.$comment}
 */
export const BENIGN_LABELS = ${JSON.stringify(benignLabels.labels)} as const;
`;

  await writeFile(
    join(OUTPUT_DIR, 'benign_labels.ts'),
    bannerFor('this file', 'decisions/benign_labels.json') +
      '\n' +
      benignLabelsBody
  );

  await writeFile(
    join(OUTPUT_DIR, 'index.ts'),
    BARREL_BANNER +
      `\nexport * from './decisions';\nexport * from './benign_labels';\n`
  );

  console.log('\n💅 Formatting...');
  run(`pnpm exec prettier --write "${OUTPUT_DIR}/**/*.ts"`);

  console.log('\n✨ Complete!\n');
}

main().catch((err) => {
  console.error('❌ Failed:', err);
  process.exit(1);
});
